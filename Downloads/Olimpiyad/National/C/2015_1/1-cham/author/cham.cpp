#include <iostream>
#include <algorithm>
#include <cstdio>
using namespace std;
long long N,Sum,p,br;
long long a[100001];

int main () {
int i;

	cin>>N;
	for (i=1;i<=N;i++) cin>>a[i];
	br=1; p=a[N]; Sum=p;
	for (i=N-1;i>=1;i--) {
		if (a[i]!=a[i+1]){ 
			p=a[i]-br;
		}
		br++;
		if (p<=0) break;
		Sum+=p;
	}
	cout<<Sum<<endl;
return 0;
}
